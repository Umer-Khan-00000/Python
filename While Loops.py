
                                # ----- WHILE LOOPS IN PYTHON : -----

# Example Of 1st While Loop

i = 0
while (i<3):
  print(i)
  i = i + 1

print("Done with the loop") 


# Example Of 2nd While Loop 

i = int(input("Enter the number: "))  # 2
while(i<=38):
  i = int(input("Enter the number again: "))  # 4
  print(i)
print("Done with the loop")

# Example Of 3rd While Loop

count = 5
while(count > 0):
  print(count)
  count = count - 1

print("I am exiting the loop")


